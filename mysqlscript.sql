CREATE TABLE employee (
  sno int(11) NOT NULL AUTO_INCREMENT unique,
  empid varchar(10) NOT NULL,
  empname varchar(30) DEFAULT NULL,
  empdept varchar(10) DEFAULT NULL,
  empmobile varchar(10) DEFAULT NULL,
  empemail varchar(30) DEFAULT NULL,
  PRIMARY KEY (empid)
);
CREATE TABLE emplogin (
  sno int(11) NOT NULL AUTO_INCREMENT unique,
  loginid varchar(8) DEFAULT NULL unique,
  password varchar(225) default "anits@123",
  empid varchar(10) DEFAULT NULL,
  role varchar(10) default "faculty",
  KEY empid (empid),
  CONSTRAINT emplogin FOREIGN KEY (empid) REFERENCES employee (empid)
);
